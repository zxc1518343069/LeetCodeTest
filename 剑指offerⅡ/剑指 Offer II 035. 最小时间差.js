/**
 * @param {string[]} timePoints
 * @return {number}
 */
var findMinDifference = function (timePoints) {
  // 特殊判断 24 * 60  最多 超过这个一定有 重复 则为0
  // 超过 12 * 60  则一定有一个数为 差值为1  参考钟表 但是要去重
  const n = timePoints.length;
  if (n > 1440) {
    return 0;
  }

  const secArr = timePoints.map((item) => {
    let arr = item.split(":");
    return arr[0] * 60 + arr[1] * 1;
  });
  // 排序 结果为时间从小到大
  secArr.sort((a, b) => a - b);
  // 首尾时间差
  console.log(timePoints.length, secArr);
  let result = secArr[0] + 1440 - secArr[secArr.length - 1];
  // 比较最小时差
  for (let i = 0; i < secArr.length - 1; i++) {
    let res = secArr[i + 1] - secArr[i];
    if (result > res) result = res;
  }
  return result;
};
